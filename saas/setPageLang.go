// translateToThai translates the "cn" field to Thai and adds the "th" field
func translateToThai(data interface{}) {
	switch data := data.(type) {
	case map[string]interface{}:
		if cnValue, ok := data["cn"].(string); ok {
			if thValue, exists := translations[cnValue]; exists {
				data["th"] = thValue
			}
		}
		for _, value := range data {
			translateToThai(value)
		}
	case []interface{}:
		for _, item := range data {
			translateToThai(item)
		}
	}
}

